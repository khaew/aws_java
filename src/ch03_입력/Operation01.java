package ch03_입력;

public class Operation01 {

	public static void main(String[] args) {
int num = 10;

		

		System.out.println(3 / 2);  //연산자는 항상 띄워쓴다.(가독성)

		

		System.out.println(num);

		System.out.println(++num);  //선처리

		System.out.println(num++);  //후처리(많이씀)

		System.out.println(num);

		num++;

		System.out.println(num);


	}

}
